// 数组的标准库方法

// push()           在数组末尾添加元素(改变原数组)
// pop()            在数组末尾弹出元素(改变原数组)
// shift()          在数组头部弹出元素(改变原数组)
// unshift()        在数组头部添加元素(改变原数组)
// join()           以指定参数作为分隔符,生成数组字符串(不改变原数组)
// concat()         指定数组和原数组合并(不改变原数组)
// reverse()        反转数组(改变原数组)
// slice()          提取数组部分元素(不改变原数组)
// splice()         删除数组部分元素,在删除位置添加新元素(改变原数组)
// sort()           数组排序;默认按照字符串字典排序(改变原数组)
// map()            数组元素依次传入参数函数,返回函数返回值组成的新数组(不改变原数组)
// forEach()        数组遍历操作,返回数组元素操作后的数组(不改变原数组)
// filter()         数组元素条件过滤,返回满足条件的数组元素组成的新数组(不改变原数组)
// some()           判断是否存在数组元素满足条件(不改变原数组)
// every()          判断是否所有数组元素满足条件(不改变原数组)
// reduce()         从左到右依次处理数组元素返回累计值(不改变原数组)
// reduceRight()    从右到左依次处理数组元素返回累计值(不改变原数组)
// indexOf()        返回指定元素首次出现的索引值(不改变原数组)
// lastIndexOf()    返回指定元素最后出现的索引值(不改变原数组)
// copyWithin()     将指定位置成员复制到目标位置(覆盖操作)并返回当前数组(改变原数组)
// find()           查找首个符合条件的数组元素,查找失败返回undefined(不改变原数组)
// findIndex()      查找首个符合条件的数组元素索引位置,查找失败返回-1(不改变原数组)
// findLast()       倒序查找首个符合条件的数组元素，查找失败返回undefined(不改变原数组)
// findLastIndex()  倒序查找首个符合条件的数组元素索引位置,查找失败返回-1(不改变原数组)
// fill()           使用指定值填充数组;可以指定填充的起止位置(改变原数组)
// entries()        获取数组键值对的迭代器对象(不改变原数组)
// keys()           获取数组键名的迭代器对象(不改变原数组)
// values()         获取数组元素的迭代器对象(不改变原数组)
// includes()       判断数组是否包含指定元素;可以指定查找的起始位置(不改变原数组)
// flat()           返回嵌套数组展平后的一维数组(不改变原数组)
// flatMap()        对数组每个元素执行指定函数后再执行flat()返回(不改变原数组)
// at()             返回指定索引位置的数组元素;索引参数为负数时从末尾开始倒序查找(不改变原数组)
// toReversed()     reversed()的不改变原数组形式,返回数组拷贝(不改变原数组)
// toSorted()       sort()的不改变原数组形式,返回数组拷贝(不改变原数组)
// toSpliced()      spliced()的不改变原数组形式,返回数组拷贝(不改变原数组)
// with()           spliced()只替换单个元素的不改变原数组形式,返回数组拷贝(不改变原数组)
